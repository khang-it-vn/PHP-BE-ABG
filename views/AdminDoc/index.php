        <h1 id="getting-support"><a href="support.html#getting-support" class="header-anchor">#</a> Getting Support
        </h1>
        <p>In this page you will find how to get support and how to raise perfect support ticket to resolve your
            issues quickly with minimum conversation. We value your time üïù</p>
        <h2 id="raising-support-ticket"><a href="support.html#raising-support-ticket" class="header-anchor">#</a>
            Raising Support Ticket</h2>
        <p>Raising support ticket in support portal is as simple as commenting on post. However, If you want to get
            your issues resolved quickly please follow below guide:</p>
        <h3 id="finding-existing-solution"><a href="support.html#finding-existing-solution" class="header-anchor">#</a> Finding existing solution</h3>
        <p>There might be a case that your issue is already resolved and you are the one who is facing due to some
            issue. So before creating new support ticket please search your question / issue in our <a href="https://demos.themeselection.com/materio-mui-react-nextjs-admin-template/documentation/faq/">FAQ
                page</a> where we have listed most common questions / issues asked in our support portal.</p>
        <h3 id="ticket-type"><a href="support.html#ticket-type" class="header-anchor">#</a> Ticket Type</h3>
        <p>Add ticket type to your ticket title to let community understand what your ticket is about. This also helps
            to find related tickets using ticket type.</p>
        <p>So, your ticket till will be like <code>[question] how to change theme?</code></p>
        <p>Some common ticket types are listed below:</p>
        <ul>
            <li><strong>question</strong> - If you are asking question</li>
            <li><strong>installation</strong> - If you have anything related to installation</li>
            <li><strong>auth</strong> - If you have anything related to JWT or Auth</li>
            <li><strong>acl</strong> - If you have anything related to Access Control</li>
            <li><strong>feat-req</strong> - If you have any feature request for our template</li>
            <li><strong>bug</strong> - If you find any bug in template</li>
            <li><strong>other</strong> - if your ticket isn't related to above mentioned ticket types</li>
        </ul>
        <p>Good examples can be: üíØ</p>
        <ul>
            <li>[question] How to change theme?</li>
            <li>[question] How to change i18n locale?</li>
            <li>[installation] Getting errors in npm run dev</li>
            <li>[auth] Unexpected behavior after login</li>
            <li>[other] Collapse component is not working as expected</li>
            <li>[auth] I get redirected to login page even after login on refresh</li>
        </ul>
        <h3 id="new-issue-new-ticket"><a href="support.html#new-issue-new-ticket" class="header-anchor">#</a> New
            Issue - New ticket</h3>
        <p>Always consider creating a new support ticket for the question or issue you might have that is not related
            to your currently open ticket.</p>
        <p>Let's say you opened ticket for installation issue and want to ask about some component usage then it will
            be better to close installation ticket and open another ticket for that component to make support portal
            more accurate and easy find place.</p>
        <h3 id="template-version"><a href="support.html#template-version" class="header-anchor">#</a> Template Version
        </h3>
        <p>Mentioning which version of our template you are using will help us to answer more accurately and will
            mitigate one question from our side.</p>
        <p>Mentioning template version includes if you are using TypeScript or JavaScript, Full-Version or
            Starter-Kit. Also, if you are using some other technology with our template then mentioning it will also
            help us in resolving issue quickly.</p>
        <p>Good examples can be: üíØ</p>
        <ul>
            <li>TypeScript + Full Version</li>
            <li>Javascript + Starter Kit</li>
        </ul>
        <h3 id="sharing-code-or-snippet"><a href="support.html#sharing-code-or-snippet" class="header-anchor">#</a>
            Sharing Code or Snippet</h3>
        <p>If you want to show us some code it will be better if you can format it properly and use code block so your
            pasted code get highlighted.</p>
        <h3 id="reproducing-issue-bug-starter-kit"><a href="support.html#reproducing-issue-bug-starter-kit" class="header-anchor">#</a> Reproducing Issue/Bug - Starter-kit</h3>
        <p>If you have ticket where we need to have a look at your code like some component's certain feature is
            working in our live demo but not in your downloaded package then it will better you give us actual issue or
            reproduction steps.</p>
        <p>Best solution for this is using our Starter-kit where we can test it without any dependency. This will also
            help you to clear your issue before sending it to us.</p>
        <p>You can create zip and share with us using private link.</p>
        <div class="custom-block warning">
            <p class="custom-block-title">WARNING</p>
            <p>Never upload our package or modified version of our package on public sites where others can view or
                download it.</p>
        </div>
